#!/usr/bin/env python3
"""render-enricher-env.py — render DomainScope's colocated enricher URL
overrides from services.json instead of hand-writing them (ADR 0233,
phase 1).

Each `SERVICE_<ID>_URL` line conflates four concerns; this generator
composes them from their real owners:

    http://{FLEET_INTERNAL_HOST}:{registry.host_port}{path}
            ?{registry.auth.query_param}={token}&{target_param}={target}

  - FLEET_INTERNAL_HOST   one policy value (env; default 10.10.10.20)
  - host_port             services.json  (the bit that drifts on redeploy)
  - auth.query_param      services.json  (api_key for the whole set today)
  - path + target_param   the enricher's call CONTRACT — lives here as a
                          descriptor (in production this is the ADR-0232
                          descriptor row, NOT services.json `example_path`,
                          which is illustrative and has drifted)

Usage:
    render-enricher-env.py [--services PATH]            # print rendered block
    render-enricher-env.py --check ENVFILE [--services] # diff vs live .env; exit 1 on drift

The `--check` mode proves parity: run it against the live .env and it must
reproduce the existing 11 lines byte-for-byte.
"""
from __future__ import annotations

import argparse
import json
import os
import re
import sys

# Call CONTRACT per colocated enricher: (path, target_param).
# Seeded from the live .env (authoritative for the actual call), which is
# what production would migrate into the ADR-0232 descriptor table. The
# membership of this map is the "colocated" set; everything else resolves
# to its public *.0crawl.com URL with no override.
CALL_SHAPE: dict[str, tuple[str, str]] = {
    "api-extractor":                 ("/go_api_extractor", "target"),
    "asn-lookup":                    ("/",                 "url"),
    "cdn-detector":                  ("/",                 "url"),
    "cookie-checker":                ("/",                 "url"),
    "domain-structured-data-rollup": ("/scan",             "target"),
    "revenue-model":                 ("/",                 "url"),
    "seasonal-detector":             ("/",                 "target"),
    "security-headers":              ("/",                 "url"),
    "shipping-regions":              ("/",                 "target"),
    "social-graph":                  ("/",                 "url"),
    "tos-finder":                    ("/",                 "url"),
}

MARKER = "# >>> generated by render-enricher-env.py (ADR 0233) — do not hand-edit >>>"
MARKER_END = "# <<< end generated block <<<"


def env_key(service_id: str) -> str:
    return "SERVICE_" + service_id.upper().replace("-", "_") + "_URL"


def load_registry(path: str) -> dict[str, dict]:
    with open(path) as fh:
        data = json.load(fh)
    return {s["id"]: s for s in data if "id" in s}


def render(registry: dict[str, dict], internal_host: str) -> dict[str, str]:
    """id-keyed map of envline -> URL, composed from registry + descriptor."""
    out: dict[str, str] = {}
    missing: list[str] = []
    for sid, (path, target_param) in sorted(CALL_SHAPE.items()):
        svc = registry.get(sid)
        if not svc:
            missing.append(sid)
            continue
        port = svc.get("host_port")
        qp = (svc.get("auth") or {}).get("query_param", "api_key")
        url = (
            f"http://{internal_host}:{port}{path}"
            f"?{qp}={{token}}&{target_param}={{target}}"
        )
        out[env_key(sid)] = url
    if missing:
        print(f"WARN: not in registry, skipped: {', '.join(missing)}", file=sys.stderr)
    return out


def rendered_block(rendered: dict[str, str]) -> str:
    lines = [MARKER]
    for k in sorted(rendered):
        lines.append(f"{k}={rendered[k]}")
    lines.append(MARKER_END)
    return "\n".join(lines)


def extract_live(envfile: str) -> dict[str, str]:
    live: dict[str, str] = {}
    pat = re.compile(r"^(SERVICE_[A-Z0-9_]+_URL)=(http\S+)$")
    with open(envfile) as fh:
        for raw in fh:
            m = pat.match(raw.strip())
            if m:
                live[m.group(1)] = m.group(2)
    return live


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--services", default=os.getenv(
        "FLEET_SERVICES_JSON",
        "/root/workspace/services-registry/services.json"))
    ap.add_argument("--internal-host",
                    default=os.getenv("FLEET_INTERNAL_HOST", "10.10.10.20"))
    ap.add_argument("--check", metavar="ENVFILE",
                    help="diff rendered output against the SERVICE_*_URL lines in ENVFILE")
    args = ap.parse_args()

    registry = load_registry(args.services)
    rendered = render(registry, args.internal_host)

    if not args.check:
        print(rendered_block(rendered))
        return 0

    live = extract_live(args.check)
    # Only compare the keys this generator owns (the colocated set).
    keys = set(rendered)
    drift = []
    for k in sorted(keys | (set(live) & keys)):
        r, l = rendered.get(k), live.get(k)
        if r != l:
            drift.append((k, l, r))
    if drift:
        print(f"DRIFT: {len(drift)} line(s) differ between live .env and registry render:")
        for k, l, r in drift:
            print(f"  {k}\n    live:      {l}\n    rendered:  {r}")
        return 1
    print(f"PARITY OK: all {len(keys)} colocated overrides reproduce the live .env byte-for-byte.")
    print(f"  (host_port + auth.query_param sourced from {args.services}; "
          f"internal_host={args.internal_host})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
